import math

def soma (n1, n2):
    print('Resultado: ', n1 + n2)

def sub (n1, n2):
    print ('Resultado: ', n1 - n2)

def mult (n1, n2):
    print ('Resultado: ', n1 * n2)

def div (n1, n2):
    print ('Resultado: ', n1 / n2)

def pow (n1, n2):
    print ('Resultado: ', n1 ** n2)

def raiz(n1):
    print ('Resultado: ', math.sqrt(n1))

def raizCub(n1):
    print ('Resultado: ', math.cbrt(n1))

def sen(n1):
    print ('Resultado: ', math.sin(n1))

def cos(n1):
    print ('Resultado: ', math.cos(n1))

def tan(n1):
    print ('Resultado: ', math.tan(n1))

def log(n1):
    print ('Resultado: ', math.log(n1))

def logBase(n1, n2):
    print ('Resultado: ', math.log(n1, n2))

def log10(n1):
    print ('Resultado: ', math.log10(n1))

def fat(n1):
    print ('Resultado: ', math.factorial(n1))

def exp(n1):
    print ('Resultado: ', math.exp(n1))