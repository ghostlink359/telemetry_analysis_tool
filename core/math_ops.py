import math

def soma (n1, n2):
    return n1 + n2

def sub (n1, n2):
    return n1 - n2

def mult (n1, n2):
    return n1 * n2

def div (n1, n2):
    return  n1 / n2

def pow (n1, n2):
    return n1 ** n2

def raiz(n1):
    return math.sqrt(n1)

def raizCub(n1):
    return math.cbrt(n1)

def tan(n1):
    return math.tan(n1)

def fat(n1):
    return math.factorial(n1)

def exp(n1):
    return math.exp(n1)